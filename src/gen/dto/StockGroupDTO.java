    public String getStockGroup_Under() {
        return stockGroup_Under;
    }

    /**
     * @param stockGroup_Under the stockGroup_Under to set
     */
    public void setStockGroup_Under(String stockGroup_Under) {
        this.stockGroup_Under = stockGroup_Under;
    }
