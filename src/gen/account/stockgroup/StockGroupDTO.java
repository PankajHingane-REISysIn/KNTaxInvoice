package gen.account.stockgroup;

/**
 *
 * @author pc5
 */
public class StockGroupDTO {

    private String stockGroupName = "";
    private String stockGroupAlias = "";
    private String stockGroupUnder = "";
    private Integer stockGroupID = 0;

    /**
     * @return the stockGroupName
     */
    public String getStockGroupName() {
        return stockGroupName;
    }

    /**
     * @param stockGroupName the stockGroupName to set
     */
    public void setStockGroupName(String stockGroupName) {
        this.stockGroupName = stockGroupName;
    }

    /**
     * @return the stockGroupAlias
     */
    public String getStockGroupAlias() {
        return stockGroupAlias;
    }

    /**
     * @param stockGroupAlias the stockGroupAlias to set
     */
    public void setStockGroupAlias(String stockGroupAlias) {
        this.stockGroupAlias = stockGroupAlias;
    }

    /**
     * @return the stockGroupUnder
     */
    public String getStockGroupUnder() {
        return stockGroupUnder;
    }

    /**
     * @param stockGroupUnder the stockGroupUnder to set
     */
    public void setStockGroupUnder(String stockGroupUnder) {
        this.stockGroupUnder = stockGroupUnder;
    }

    /**
     * @return the stockGroupID
     */
    public Integer getStockGroupID() {
        return stockGroupID;
    }

    /**
     * @param stockGroupID the stockGroupID to set
     */
    public void setStockGroupID(Integer stockGroupID) {
        this.stockGroupID = stockGroupID;
    }


    
}
