    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        map_Field_With_ID.clear();
        if (checkBoxReceiptnoEditable.isSelected()) {
            map_Field_With_ID.put(gen.other.AdSuMuDiSettingsOLD.AdSuMuDiConstant.RECEIPT_SETTINGS, "" + 1);
        } else {
            map_Field_With_ID.put(gen.other.AdSuMuDiSettingsOLD.AdSuMuDiConstant.RECEIPT_SETTINGS, "" + 0);
        }

        if (checkBoxAutoInrementVoucherNumber.isSelected()) {
            map_Field_With_ID.put(gen.other.AdSuMuDiSettingsOLD.AdSuMuDiConstant.VOUCHER_NUMBER_SETTING, "" + 1);
        } else {
            map_Field_With_ID.put(gen.other.AdSuMuDiSettingsOLD.AdSuMuDiConstant.VOUCHER_NUMBER_SETTING, "" + 0);
        }
        //AdSuMuDiSettingsDAO.insert_Receipt_Settings(map_Field_With_ID);
        gen.other.AdSuMuDiSettingsOLD.AdSuMuDiConstant.getValuesFromTable();
        JOptionPane.showMessageDialog(this, "Setting SuccessFully Submitted");
        set_Value_CheckBox();
    }//GEN-LAST:event_btnsubmitActionPerformed

